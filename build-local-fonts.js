// build-local-fonts.js
// 从 node_modules/@fontsource/* copy 11 个 Arabic 字体的 woff2/woff 文件到 fonts/
// 生成 fonts/fonts.css（11 个 @font-face，font-display: block 强制阻塞加载）
// 目的：canvas 渲染时不再 fallback，用户切字体立即看到差异，Puppeteer gallery 图字体真实

const fs = require('fs');
const path = require('path');

const FONTS = [
  { pkg: 'amiri',                family: 'Amiri' },
  { pkg: 'aref-ruqaa',           family: 'Aref Ruqaa' },
  { pkg: 'cairo',                family: 'Cairo' },
  { pkg: 'lateef',               family: 'Lateef' },
  { pkg: 'markazi-text',         family: 'Markazi Text' },
  { pkg: 'mirza',                family: 'Mirza' },
  { pkg: 'noto-kufi-arabic',     family: 'Noto Kufi Arabic' },
  { pkg: 'noto-naskh-arabic',    family: 'Noto Naskh Arabic' },
  { pkg: 'noto-nastaliq-urdu',   family: 'Noto Nastaliq Urdu' },
  { pkg: 'reem-kufi',            family: 'Reem Kufi' },
  { pkg: 'scheherazade-new',     family: 'Scheherazade New' },
];

const OUT_DIR = path.join(__dirname, 'fonts');
if (!fs.existsSync(OUT_DIR)) fs.mkdirSync(OUT_DIR, { recursive: true });

let css = '/* Local Arabic fonts — generated by build-local-fonts.js */\n';
let copied = 0;

for (const { pkg, family } of FONTS) {
  const woff2 = `${pkg}-arabic-400-normal.woff2`;
  const woff  = `${pkg}-arabic-400-normal.woff`;
  const srcDir = path.join(__dirname, 'node_modules', '@fontsource', pkg, 'files');
  const dstDir = OUT_DIR;

  fs.copyFileSync(path.join(srcDir, woff2), path.join(dstDir, woff2));
  fs.copyFileSync(path.join(srcDir, woff),  path.join(dstDir, woff));
  copied += 2;

  css += `
@font-face {
  font-family: '${family}';
  font-style: normal;
  font-weight: 400;
  font-display: block;
  src: url('./${woff2}') format('woff2'),
       url('./${woff}') format('woff');
}
`;
}

fs.writeFileSync(path.join(OUT_DIR, 'fonts.css'), css);
console.log(`✅ Copied ${copied} font files + wrote fonts/fonts.css (11 @font-face)`);
